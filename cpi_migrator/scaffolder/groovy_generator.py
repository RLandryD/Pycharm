@dataclass
class GroovyScript:
    filename: str
    content: str
    script_type: str
    interface_name: str
    warnings: list[str] = field(default_factory=list)
    local_path: Optional[Path] = None


class GroovyGenerator:

    def __init__(self, output_dir: str):
        self.output_dir = Path(output_dir) / "groovy"
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def generate(
        self,
        assessment,
        cfg=None,
        mapping_content: str = "",
        jar_class: str = "",
        jar_name: str = "",
    ) -> list[GroovyScript]:
        """Generate all Groovy scripts needed for an interface."""
        iface   = assessment.interface
        scripts = []

        # Main transformation script
        if iface.mapping_program:
            script_type = detect_mapping_type(iface.mapping_program, mapping_content)
            main_script = self._generate_script(
                iface, cfg, script_type,
                jar_class=jar_class, jar_name=jar_name,
            )
            scripts.append(main_script)

        # Always generate exception handler
        error_script = self._generate_exception_handler(iface)
        scripts.append(error_script)

        # Header manipulation if adapters require it
        if iface.sender_adapter in ("IDoc", "RFC") or \
           iface.receiver_adapter in ("IDoc", "RFC"):
            header_script = self._generate_header_script(iface)
            scripts.append(header_script)

        # Write all to disk
        for script in scripts:
            path = self.output_dir / script.filename
            path.write_text(script.content, "utf-8")
            script.local_path = path

        return scripts

    def generate_all(
        self,
        assessments: list,
        configs: dict = None,
    ) -> dict[str, list[GroovyScript]]:
        """Generate scripts for all assessments. Returns {iface_name: [scripts]}."""
        configs = configs or {}
        results = {}
        for a in assessments:
            if not a.interface.mapping_program:
                continue
            try:
                cfg     = configs.get(a.interface.name)
                scripts = self.generate(a, cfg=cfg)
                results[a.interface.name] = scripts
                logger.info("Generated %d Groovy script(s) for %s",
                            len(scripts), a.interface.name)
            except Exception as exc:
                logger.error("Groovy generation failed for %s: %s",
                             a.interface.name, exc)
        return results

    # ── Script builders ───────────────────────────────────────────────

    def _generate_script(
        self,
        iface,
        cfg,
        script_type: str,
        jar_class: str = "",
        jar_name: str = "",
    ) -> GroovyScript:
        safe_name = re.sub(r"[^\w]", "_", iface.name)
        filename  = f"{safe_name}_transform.groovy"
        warnings  = []

        header = GROOVY_HEADER.format(
            original_name=iface.mapping_program or iface.name,
            interface_name=iface.name,
            sender_system=iface.sender_system or "SENDER",
            sender_adapter=iface.sender_adapter,
            receiver_system=iface.receiver_system or "RECEIVER",
            receiver_adapter=iface.receiver_adapter,
        )

        if script_type == "xml_transform":
            ns        = iface.namespace or "http://company.com/integration"
            root      = iface.name.replace(" ", "").replace("-", "") or "Message"
            body      = XML_TRANSFORM_TEMPLATE.format(
                target_namespace=ns,
                target_root=root,
                field_mappings=self._generate_field_mappings(iface, cfg),
                sender_system=iface.sender_system or "SENDER",
                receiver_system=iface.receiver_system or "RECEIVER",
                message_type=iface.message_interface or iface.name,
                source_namespace=ns,
            )

        elif script_type == "header":
            body = HEADER_MANIPULATION_TEMPLATE.format()

        elif script_type == "value_mapping":
            body = VALUE_MAPPING_TEMPLATE.format()

        elif script_type == "content_enricher":
            enricher_url = (cfg.receiver_connectivity.address if cfg else "") or "[ENRICHER_URL]"
            body = CONTENT_ENRICHER_TEMPLATE.format(enricher_url=enricher_url)

        elif script_type == "java_wrapper":
            if not jar_class:
                warnings.append("Java class name not provided — wrapper is incomplete")
            body = JAVA_WRAPPER_TEMPLATE.format(
                jar_name=jar_name or "[mapping.jar]",
                java_class=jar_class or "com.company.mapping.MappingClass",
            )
            warnings += [
                "Binary attachment processing (PDF/Office) will NOT work in wrapper",
                "File system I/O will NOT work — must rewrite natively",
                "Test wrapper thoroughly before production deployment",
            ]

        else:
            body = XML_TRANSFORM_TEMPLATE.format(
                target_namespace=iface.namespace or "http://company.com",
                target_root="Message",
                field_mappings="                // TODO: Add field mappings",
                sender_system=iface.sender_system or "SENDER",
                receiver_system=iface.receiver_system or "RECEIVER",
                message_type=iface.name,
                source_namespace=iface.namespace or "",
            )

        return GroovyScript(
            filename=filename,
            content=header + body,
            script_type=script_type,
            interface_name=iface.name,
            warnings=warnings,
        )

    def _generate_exception_handler(self, iface) -> GroovyScript:
        safe_name = re.sub(r"[^\w]", "_", iface.name)
        content   = GROOVY_HEADER.format(
            original_name="Exception Handler",
            interface_name=iface.name,
            sender_system=iface.sender_system or "SENDER",
            sender_adapter=iface.sender_adapter,
            receiver_system=iface.receiver_system or "RECEIVER",
            receiver_adapter=iface.receiver_adapter,
        ) + EXCEPTION_HANDLER_TEMPLATE.format(
            interface_name=iface.name,
            sender_system=iface.sender_system or "SENDER",
            receiver_system=iface.receiver_system or "RECEIVER",
        )
        return GroovyScript(
            filename=f"{safe_name}_error_handler.groovy",
            content=content,
            script_type="exception_handler",
            interface_name=iface.name,
        )

    def _generate_header_script(self, iface) -> GroovyScript:
        safe_name = re.sub(r"[^\w]", "_", iface.name)
        content   = GROOVY_HEADER.format(
            original_name="Header Manipulation",
            interface_name=iface.name,
            sender_system=iface.sender_system or "SENDER",
            sender_adapter=iface.sender_adapter,
            receiver_system=iface.receiver_system or "RECEIVER",
            receiver_adapter=iface.receiver_adapter,
        ) + HEADER_MANIPULATION_TEMPLATE.format()
        return GroovyScript(
            filename=f"{safe_name}_headers.groovy",
            content=content,
            script_type="header",
            interface_name=iface.name,
        )

    @staticmethod
    def _generate_field_mappings(iface, cfg) -> str:
        """Generate commented field mapping stubs based on known interface info."""
        lines = []
        ns = iface.namespace or "http://company.com"

        if iface.sender_adapter == "IDoc" and cfg:
            lines += [
                f"                // IDoc → XML field mappings",
                f"                // TODO: Map E1 segment fields to target schema",
                f"                'DocumentNumber' sourceXml.IDOC.EDI_DC40.DOCNUM.text()",
                f"                'MessageType'    sourceXml.IDOC.EDI_DC40.MESTYP.text()",
            ]
        elif iface.message_interface:
            lines += [
                f"                // TODO: Map fields from {iface.message_interface}",
                f"                // to target schema",
                f"                'Field1' sourceXml.Payload.Field1.text()",
                f"                'Field2' sourceXml.Payload.Field2.text()",
            ]
        else:
            lines += [
                f"                // TODO: Add field mappings",
                f"                // Pattern: 'TargetField' sourceXml.path.to.field.text()",
            ]

        return "\n".join(lines)
