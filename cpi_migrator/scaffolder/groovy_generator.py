"""
scaffolder/groovy_generator.py

Converts PI/PO Java mapping patterns to native Apache Groovy scripts
for SAP Cloud Integration.

Handles:
  1. Standard XML transformation (XMLSlurper + XMLBuilder pattern)
  2. Header/property manipulation
  3. Exception handling with MPL logging
  4. Value mapping lookups
  5. Content enricher patterns
  6. Legacy Java wrapper (for complex .jar files)

Output: .groovy files ready to add as Script step in CPI iFlow
"""
