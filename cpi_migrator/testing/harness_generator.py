# ---------------------------------------------------------------------------
# Generator
# ---------------------------------------------------------------------------

@dataclass
class HarnessResult:
    interface_name: str
    payload_path: Path
    test_path: Path
    payload_format: str


class HarnessGenerator:

    def __init__(self, output_dir: str):
        self.output_dir  = Path(output_dir)
        self.tests_dir   = self.output_dir / "tests"
        self.payload_dir = self.tests_dir / "payloads"
        self.tests_dir.mkdir(parents=True, exist_ok=True)
        self.payload_dir.mkdir(parents=True, exist_ok=True)

    def generate(
        self,
        assessment,             # MigrationAssessment
        cfg=None,               # InterfaceConfig | None
    ) -> HarnessResult:
        iface     = assessment.interface
        safe_name = iface.name.replace(" ", "_").replace("-", "_")
        now       = datetime.now()

        # ── Payload ─────────────────────────────────────────────────
        payload_format, payload_content, payload_ext = self._build_payload(
            iface, cfg, now
        )
        payload_filename = f"{safe_name}_sample{payload_ext}"
        payload_path     = self.payload_dir / payload_filename
        payload_path.write_text(payload_content, "utf-8")

        # ── Pytest file ──────────────────────────────────────────────
        test_content = self._build_pytest(iface, cfg, safe_name,
                                          payload_filename, payload_format)
        test_path = self.tests_dir / f"test_{safe_name}.py"
        test_path.write_text(test_content, "utf-8")

        logger.info("Generated test harness for %s", iface.name)
        return HarnessResult(
            interface_name=iface.name,
            payload_path=payload_path,
            test_path=test_path,
            payload_format=payload_format,
        )

    def generate_all(self, assessments, configs: dict = None) -> list[HarnessResult]:
        configs = configs or {}
        results = []
        postman = dict(POSTMAN_TEMPLATE)
        postman["item"] = []

        for a in assessments:
            try:
                result = self.generate(a, cfg=configs.get(a.interface.name))
                results.append(result)
                postman["item"].append(
                    self._build_postman_item(a.interface, result.payload_path,
                                             result.payload_format)
                )
            except Exception as exc:
                logger.error("Harness generation failed for %s: %s",
                             a.interface.name, exc)

        # Write Postman collection
        postman_path = self.tests_dir / "postman_collection.json"
        postman_path.write_text(json.dumps(postman, indent=2), "utf-8")
        logger.info("Generated %d test harnesses", len(results))
        return results

    # ── Payload builders ─────────────────────────────────────────────

    def _build_payload(self, iface, cfg, now) -> tuple[str, str, str]:
        adapter = iface.sender_adapter
        ns      = iface.namespace or "http://company.com/integration"
        root    = iface.name.replace(" ", "").replace("-", "")

        if adapter == "IDoc":
            idoc_type = (cfg.message.idoc_type if cfg else "") or "ORDERS05"
            msg_type  = (cfg.message.idoc_message_type if cfg else "") or "ORDERS"
            content   = IDOC_PAYLOAD_TEMPLATE.format(
                interface_name=iface.name,
                idoc_type=idoc_type,
                message_type=msg_type,
                sender_system=iface.sender_system or "SENDER",
                receiver_system=iface.receiver_system or "RECEIVER",
                date=now.strftime("%Y%m%d"),
                time=now.strftime("%H%M%S"),
            )
            return "idoc", content, ".xml"

        msg_format = (cfg.message.format if cfg else "XML") or "XML"

        if msg_format == "JSON":
            content = JSON_PAYLOAD_TEMPLATE.format(
                interface_name=iface.name,
                message_id=f"MSG-{now.strftime('%Y%m%d%H%M%S')}",
                timestamp=now.isoformat(),
                sender_system=iface.sender_system or "SENDER",
                receiver_system=iface.receiver_system or "RECEIVER",
            )
            return "json", content, ".json"

        # Default XML
        content = XML_PAYLOAD_TEMPLATE.format(
            interface_name=iface.name,
            root_element=root or "Message",
            namespace=ns,
            message_id=f"MSG-{now.strftime('%Y%m%d%H%M%S')}",
            timestamp=now.isoformat(),
            sender_system=iface.sender_system or "SENDER",
            receiver_system=iface.receiver_system or "RECEIVER",
            sender_adapter=iface.sender_adapter,
            receiver_adapter=iface.receiver_adapter,
        )
        return "xml", content, ".xml"

    def _build_pytest(self, iface, cfg, safe_name,
                      payload_filename, payload_format) -> str:
        class_name   = "".join(w.capitalize() for w in safe_name.split("_"))
        content_type = "application/xml"
        if payload_format == "json":
            content_type = "application/json"

        format_specific = ""
        if payload_format == "xml":
            format_specific = '''\n        import xml.etree.ElementTree as ET
        ET.fromstring(PAYLOAD_FILE.read_bytes())  # raises if invalid XML'''
        elif payload_format == "json":
            format_specific = '''\n        import json
        json.loads(PAYLOAD_FILE.read_text())  # raises if invalid JSON'''

        adapter_tests = self._adapter_specific_tests(iface, cfg)

        return PYTEST_TEMPLATE.format(
            interface_name=iface.name,
            safe_name=safe_name,
            iflow_id=safe_name,
            payload_filename=payload_filename,
            content_type=content_type,
            class_name=class_name,
            sender_adapter=iface.sender_adapter,
            receiver_adapter=iface.receiver_adapter,
            format_lower=payload_format,
            format_specific_test=format_specific,
            adapter_specific_tests=adapter_tests,
        )

    @staticmethod
    def _adapter_specific_tests(iface, cfg) -> str:
        tests = []
        if iface.sender_adapter == "IDoc":
            tests.append('''
    def test_idoc_structure(self):
        """Verify IDoc payload has required EDI_DC40 control record."""
        content = PAYLOAD_FILE.read_text("utf-8")
        assert "EDI_DC40" in content, "Missing IDoc control record EDI_DC40"
        assert "IDOCTYP" in content, "Missing IDOCTYP field"''')

        if iface.sender_adapter in ("File", "SFTP", "FTP"):
            tests.append('''
    def test_file_payload_encoding(self):
        """Verify file payload is valid UTF-8."""
        content = PAYLOAD_FILE.read_bytes()
        content.decode("utf-8")  # raises if not valid UTF-8''')

        if cfg and cfg.reliability.retry_enabled:
            tests.append(f'''
    def test_retry_configuration(self):
        """Document expected retry behaviour for this interface."""
        # Max retries: {cfg.reliability.retry_max_attempts}
        # Delay: {cfg.reliability.retry_delay_sec}s
        # This test documents the expected retry config — verify in CPI Monitor
        assert True  # Placeholder — verify in CPI Message Monitor after deployment''')

        return "\n".join(tests)

    @staticmethod
    def _build_postman_item(iface, payload_path: Path, fmt: str) -> dict:
        safe = iface.name.replace(" ", "_")
        return {
            "name": iface.name,
            "request": {
                "method": "POST",
                "header": [
                    {"key": "Content-Type",
                     "value": "application/json" if fmt == "json" else "application/xml"},
                    {"key": "Authorization",
                     "value": "Bearer {{cpi_token}}"},
                ],
                "body": {
                    "mode": "raw",
                    "raw": payload_path.read_text("utf-8") if payload_path.exists() else "",
                    "options": {"raw": {"language": fmt}},
                },
                "url": {
                    "raw": f"{{{{cpi_base_url}}}}/http/{safe}",
                    "host": ["{{cpi_base_url}}"],
                    "path": ["http", safe],
                },
            },
            "response": [],
        }
