import os
import pytest
import requests
from requests.auth import HTTPBasicAuth
from pathlib import Path

# ── Configuration ─────────────────────────────────────────────────────────
BASE_URL      = os.getenv("CPI_BASE_URL", "http://localhost:8080")
CLIENT_ID     = os.getenv("CPI_CLIENT_ID", "")
CLIENT_SECRET = os.getenv("CPI_CLIENT_SECRET", "")
TOKEN_URL     = os.getenv("CPI_TOKEN_URL", "")
IFLOW_ID      = "{iflow_id}"
ENDPOINT_PATH = "/http/{safe_name}"
PAYLOAD_FILE  = Path(__file__).parent / "payloads" / "{payload_filename}"


# ── Auth helper ────────────────────────────────────────────────────────────
def get_token() -> str:
    if not CLIENT_ID or not TOKEN_URL:
        return ""
    resp = requests.post(
        TOKEN_URL,
        data={{"grant_type": "client_credentials"}},
        auth=HTTPBasicAuth(CLIENT_ID, CLIENT_SECRET),
        timeout=15,
    )
    resp.raise_for_status()
    return resp.json()["access_token"]


@pytest.fixture(scope="module")
def session():
    s = requests.Session()
    token = get_token()
    if token:
        s.headers["Authorization"] = f"Bearer {{token}}"
    s.headers["Content-Type"] = "{content_type}"
    return s


# ── Tests ──────────────────────────────────────────────────────────────────

