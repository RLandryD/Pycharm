# Detector
# ---------------------------------------------------------------------------

def detect_mapping_type(mapping_name: str, mapping_content: str = "") -> str:
    """Detect what kind of Groovy script to generate based on mapping name/content."""
    name_lower    = mapping_name.lower()
    content_lower = mapping_content.lower()

    if any(kw in name_lower for kw in ["header", "prop", "property"]):
        return "header"
    if any(kw in name_lower for kw in ["valuemap", "value_map", "lookup", "mapping_table"]):
        return "value_mapping"
    if any(kw in name_lower for kw in ["enrich", "lookup", "content"]):
        return "content_enricher"
    if any(kw in content_lower for kw in [".jar", "class ", "import java"]):
        return "java_wrapper"
    if any(kw in name_lower for kw in ["error", "exception", "fault"]):
        return "exception_handler"
    return "xml_transform"


